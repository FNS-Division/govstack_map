export function getCoordinates(location: string): [number, number] | null {
  if (!location) return null;
  const normalized = location.trim();
  // Direct match
  if (countryCoordinates[normalized]) return countryCoordinates[normalized];
  // Case-insensitive match
  const lower = normalized.toLowerCase();
  const key = Object.keys(countryCoordinates).find(k => k.toLowerCase() === lower);
  if (key) return countryCoordinates[key];
  // Partial match (country name contains the search or vice versa)
  const partialKey = Object.keys(countryCoordinates).find(k =>
    k.toLowerCase().includes(lower) || lower.includes(k.toLowerCase())
  );
  if (partialKey) return countryCoordinates[partialKey];
  return null;
}
